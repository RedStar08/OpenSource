%*************身长-体重模型***********************
s = 10			%初始体重10kg
r = 1.25		%增长因子(由初始10kg，一年后30-40kg求得)
K = 12000		%理论最大重量
t = 0:0.1:15	%时间-年份
w_u = K*s.*exp(r*t)		%模型分子
w_d = K-s+s.*exp(r*t)	%模型分母
W = w_u ./ w_d			%体重
b = 15
L= power((W/b),1/4)		%身长与体重的关系模型
%画出身长随时间变化的模型曲线
plot(t,L)
xlabel('time/year')
ylabel('L(t)/m')
title('Length-time')